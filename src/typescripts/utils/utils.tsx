export function formatDate(date: string): string {
    const cdate = new Date(date)
    const options = {
        year: "numeric",
        month: "short",
        day: "numeric",
        hour12: false,
    } as const
    date = cdate.toLocaleDateString("en-us", options)
    return date
}

export function formatBytes(bytes: number, decimals = 2) {
    if (!+bytes) return "0 Bytes"

    const k = 1024
    const dm = decimals < 0 ? 0 : decimals
    const sizes = [
        "Bytes",
        "KiB",
        "MiB",
        "GiB",
        "TiB",
        "PiB",
        "EiB",
        "ZiB",
        "YiB",
    ]

    const i = Math.floor(Math.log(bytes) / Math.log(k))

    return `${parseFloat((bytes / Math.pow(k, i)).toFixed(dm))} ${sizes[i]}`
}

export async function convertImgToBase64(
    url: string,
    outputFormat: string = "image",
): Promise<string> {
    return new Promise((resolve, reject) => {
        const canvas = document.createElement("CANVAS") as HTMLCanvasElement
        const ctx = canvas.getContext("2d")
        const img = new Image()
        img.crossOrigin = "Anonymous"
        img.onload = function () {
            canvas.height = img.height
            canvas.width = img.width
            ctx?.drawImage(img, 0, 0)
            const dataURL = canvas.toDataURL(outputFormat)
            resolve(dataURL)
            canvas.remove() // Clean up the canvas element
        }
        img.onerror = reject
        img.src = url
    })
}

export async function getUniqueImageUrls(
    imageUrls: string[],
): Promise<string[]> {
    const uniqueBase64s = new Set<string>()
    const uniqueUrls: string[] = []

    for (const url of imageUrls) {
        try {
            const base64Img = await convertImgToBase64(url)
            if (!uniqueBase64s.has(base64Img)) {
                uniqueBase64s.add(base64Img)
                uniqueUrls.push(url)
            }
        } catch (error) {
            console.error(`Failed to convert image ${url} to base64`, error)
        }
    }

    return uniqueUrls
}

export function getMimeType(contentType: string): string {
    let mime = ""

    if (contentType.startsWith("text/")) {
        mime = textMimeTypes[contentType]
    } else if (contentType.startsWith("application/")) {
        mime = appMimeTypes[contentType]
    } else {
        mime = otherMimeTypes[contentType]
    }

    if (mime) {
        return mime
    } else {
        return "File"
    }
}

